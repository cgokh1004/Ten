package spring.model.bus;

public class BusDTO {
	
	private int busno;
	
	private String ID;
	
	private String regdate;
	
	private String startdate;
	
	private String enddate;
	
	private int price;
	
	private String b_start;
	
	private String b_end;
	
	private String ways;
	
	public int getBusno() {
		return busno;
	}

	public void setBusno(int busno) {
		this.busno = busno;
	}

	public String getID() {
		return ID;
	}

	public void setID(String iD) {
		ID = iD;
	}

	public String getRegdate() {
		return regdate;
	}

	public void setRegdate(String regdate) {
		this.regdate = regdate;
	}

	public String getStartdate() {
		return startdate;
	}

	public void setStartdate(String startdate) {
		this.startdate = startdate;
	}

	public String getEnddate() {
		return enddate;
	}

	public void setEnddate(String enddate) {
		this.enddate = enddate;
	}

	public int getPrice() {
		return price;
	}

	public void setPrice(int price) {
		this.price = price;
	}

	public String getB_start() {
		return b_start;
	}

	public void setB_start(String b_start) {
		this.b_start = b_start;
	}

	public String getB_end() {
		return b_end;
	}

	public void setB_end(String b_end) {
		this.b_end = b_end;
	}

	public String getWays() {
		return ways;
	}

	public void setWays(String ways) {
		this.ways = ways;
	}

	
}